    public void CustomerList() {
        try {
            String conn = "com.mysql.cj.jdbc.Driver";
            Class.forName(conn);
            String s = "jdbc:mysql://localhost:3306/AgriFarm";
            String rt = "root";
            Connection con = DriverManager.getConnection(s, rt, "");
            String sql = "select * from customer";
            PreparedStatement ps = (PreparedStatement) con.prepareStatement(sql);

            String n, m, a;

            DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
            tb.getDataVector().removeAllElements();

            ResultSet r = ps.executeQuery();
            while (r.next()) {

                n = r.getString("CName");
                m = r.getString("CMobile");
                a = r.getString("CAddress");
                String td[] = {n, m, a};
                tb.addRow(td);

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "not connected");
        }
    }

